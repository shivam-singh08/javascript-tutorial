//switch case
//if we miss break keyword in any of the case then it will run all cases below it except the default.
const month = 3
switch (month) {
    case 1:
        console.log('january');
        break;
    case 2:
        console.log('feb');
        break;
    case 3:
        console.log('march');
        break;

    default:
        console.log('default');
        
        break;
}
